def print_array_of_numbers():
    for i in range(1,6):
        for j in range(1,10):
            print(str(0+i-1)+'.'+str(j),end=" ")
        print(i)

# Press the green button in the gutter to run the script.
if __name__ == '__main__':
    print_array_of_numbers()





